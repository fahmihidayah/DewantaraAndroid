package com.models;

public class DataAbsensi {
	private Boolean hadir;
	private String nik;
	private Long idMataPelajaran;
	private String nim;
	private String keterangan;
	public Boolean getHadir() {
		return hadir;
	}
	public void setHadir(Boolean hadir) {
		this.hadir = hadir;
	}
	public String getNik() {
		return nik;
	}
	public void setNik(String nik) {
		this.nik = nik;
	}
	public Long getIdMataPelajaran() {
		return idMataPelajaran;
	}
	public void setIdMataPelajaran(Long idMataPelajaran) {
		this.idMataPelajaran = idMataPelajaran;
	}
	public String getNim() {
		return nim;
	}
	public void setNim(String nim) {
		this.nim = nim;
	}
	public String getKeterangan() {
		return keterangan;
	}
	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}
	
}